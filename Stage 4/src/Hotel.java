    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        Guest newGuest = new Guest(Guest.guests.get(Guest.guests.size() - 1).getGuestNum() + 1, jTextField22.getText(), jTextField23.getText(), Integer.valueOf(jTextField24.getText()));
        Guest.guests.add(newGuest);
        displayGuests();
        Reservation newReservation = new Reservation(newGuest, jCalendar1.getDate(), Integer.valueOf(jTextField26.getText()), Integer.valueOf(jTextField27.getText()));
        for (int j = 0; j < Room.rooms.size(); j++) {
            if (Room.rooms.get(j).getMaxGuests() >= newReservation.getNumPeople() && Objects.equals(Room.rooms.get(j).getRoomStatus(), "Available")) {
                newReservation.setBoard(Room.rooms.get(j));
                Room.rooms.get(j).setRoomStatus("Reserved");
                break;
            }
        }
        newReservation.setReservNumber(Reservation.reservations.get(Reservation.reservations.size() - 1).getReservNumber() + 1);
        Reservation.reservations.add(newReservation);
        displayReservations();
    }//GEN-LAST:event_jButton13ActionPerformed

