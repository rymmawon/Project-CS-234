    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:
        int j = 0;
        for(int i = 0; i < Reservation.reservations.size(); i++) {
            if(jTextField25.getText().equals(Reservation.reservations.get(i).getCustomer().getLastName())) {
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getReservNumber(), j, 0);
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getCustomer().getLastName(), j, 1);
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getStrCheckInDate(),  j, 2);
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getStrCheckOutDate(),  j, 3);
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getBoard().getRoomNumber(),  j, 4);
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getNumPeople(),  j, 5);
                jTable1.getModel().setValueAt(Reservation.reservations.get(i).getNights(),  j, 6);
                j += 1;
            }
        }
    }//GEN-LAST:event_jButton14ActionPerformed

